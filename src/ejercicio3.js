//3️⃣ Clasificación de edad
let edad = 15;
if (edad >= 0 && edad <= 12) {
  console.log("Es un niño");
} else if (edad >= 13 && edad <= 17) {
  console.log("Es un adolescente");
} else if (edad >= 18 && edad <= 64) {
  console.log("Es un adulto");
} else if (edad >= 65) {
  console.log("Es un anciano");
}
